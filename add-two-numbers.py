def add_numbers(a, b):
    """
    This function takes two numbers and returns their sum.

    Parameters:
    a (int): The first number.
    b (int): The second number.

    Returns:
    int: The sum of a and b.
    """
    return a + b

result = add_numbers(5, 10)
print("Sum:", result)  # Output: Sum: 15
